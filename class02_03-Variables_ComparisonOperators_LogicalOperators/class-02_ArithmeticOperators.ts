//ARITHMETIC OPERATORS:
var num1: number = 10;
var num2: number = 20;
// addition
var sum: number = num1 + num2;
console.log(sum);

//subtraction
var minus: number = num1 - num2;
console.log(minus);

// multiplication
var product: number = num1 * num2;
console.log(product);

// exponent (raise to power ^)
var exponent: number = 2 ** 5;
console.log(exponent);

// division
var division: number = num2 / num1;
console.log(division);

//division (remainder)
var remainder: number = num2 % 3;
console.log(remainder);
